package dca.ufrn.team.physio.model;

public class PhysioData {

	private String thumb;
	private String indexFinger;
	private String middleFinger;
	private String ringFinger;
	private String littleFinger;

	public String getThumb() {
		return thumb;
	}

	public void setThumb(String thumb) {
		this.thumb = thumb;
	}

	public String getIndexFinger() {
		return indexFinger;
	}

	public void setIndexFinger(String indexFinger) {
		this.indexFinger = indexFinger;
	}

	public String getMiddleFinger() {
		return middleFinger;
	}

	public void setMiddleFinger(String middleFinger) {
		this.middleFinger = middleFinger;
	}

	public String getRingFinger() {
		return ringFinger;
	}

	public void setRingFinger(String ringFinger) {
		this.ringFinger = ringFinger;
	}

	public String getLittleFinger() {
		return littleFinger;
	}

	public void setLittleFinger(String littleFinger) {
		this.littleFinger = littleFinger;
	}

}
